class CalculatorLibrary:

    def __init__(self):
        self.data = ""
    
    def tambah(self, nom_1, nom_2):
        return nom_1 + nom_2

    def tolak(self, nom_1, nom_2):
        return nom_1 - nom_2

    def darab(self, nom_1, nom_2):
        return nom_1 * nom_2

    def bahagi(self, nom_1, nom_2):
        return nom_1 / nom_2

    def modulo(self, nom_1, nom_2):
        return nom_1 % nom_2 